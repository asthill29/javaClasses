package com.class7;

public class whileLoopExercise3 {

	public static void main(String[] args) {
	
		//print only odd number from 49 to 100
		// "    "   even number from 100 to 1
		
		
		int z=49;
		while (z<=100) {
			System.out.print(z);
			z+=2;
		}

		
		int q = 100;
		while (q>1) {
			System.out.println(q);
			q-=2;
			}
		
		
		
	}
}
