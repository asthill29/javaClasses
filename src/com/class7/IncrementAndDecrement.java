package com.class7;

public class IncrementAndDecrement {

	public static void main(String[] args) {
		
		
		int a=10;
		
		a=a+1; // assignment
		a+=1; // shorthand assignment
		
		System.out.println(a);// output 12
		a++;// (++) --> increment operator by 1
		System.out.println(a);

		
		int b=10;
		b-=1;
		b--;// (-- ) --> decrement operator 
		System.out.println(b);
		
 // with increment and decrement operators can be used only with variable
		
		
		
		
	}

}
